<p style="text-align: justify;" align="justify">El <em><strong>IES Albarregas</strong></em> inició sus actividades en el curso 91/92. Se encuentra situado junto al camino viejo de Mirandilla, al norte de la ciudad de Mérida. Se ubica en la margen derecha del río Albarregas, de donde toma el nombre, entre el acueducto romano de los <em><strong>Milagros</strong></em> y el árabe de <em><strong>San Lázaro</strong></em>, situándose muy próximo a este último.</p>
<p style="text-align: justify;">Durante los dos primeros cursos se impartió el &#8220;Bachillerato Experimental&#8221; y a partir del curso 1993/1994 se implantó el sistema LOGSE, siendo uno de los centros pioneros de este sistema en la Comunidad Autónoma de Extremadura.</p>
<p style="text-align: justify;">Desde el comienzo se atendió también al alumnado con necesidades educativas especiales (ACNEE) y se implantaron los grupos de diversificación.</p>
<p style="text-align: justify;">Asimismo, el centro ha ido incorporando, de forma progresiva, la Formación Profesional a su oferta educativa. Se comenzó en el curso 93/94 con el módulo AG2, que posteriormente se transformó en el Ciclo Formativo de Grado Medio de Gestión Administrativa; en los cursos 95/96, 96/97 y 97/98 se amplió la oferta con los CGS de Administración y Finanzas, de Comercio Internacional, de Administración de Sistemas Informáticos y Secretariado junto con el CGM de Comercio. Además, desde el curso 2006/07 se imparte un ciclo de informática vespertino.</p>
<p style="text-align: justify;"><strong>ENTORNO SOCIAL:</strong> Las obras de mejora en el cauce del río Albarregas y el ajardinamiento de sus márgenes han supuesto un cambio sustancial en la estética de la zona. Además, el crecimiento de la ciudad, hacia el norte, ha hecho aumentar considerablemente la población en el área de influencia del Instituto. Otros edificios públicos en la zona son: la Factoría Joven y el proyecto Mérida III Milenio que ha convertido esta zona en el nuevo Centro Administrativo de la ciudad.</p>

                <p align="justify">El <em><strong>IES Albarregas</strong> </em>abrió sus puertas en el curso 1991/1992. Acabamos de cumplir veinticinco años en el curso 15-16 y un resumen de esos años<em> <a href="https://www.youtube.com/watch?v=-NTIhW8C9cs">se puede visitar aquí</a></em>.<br />
                    Nuestro nombre se lo debemos a la situación a la orilla del río <em>Albarregas</em>, entre el acueducto de <em>San Lázaro</em> y el de<em> los Milagros</em>.</p>
                <p style="text-align: justify;">Ha sido centro pionero a lo largo de los últimos años en infinidad de acontecimientos y, hoy por hoy, es un claro referente educativo y cultural dentro de Mérida. Nos situamos en una zona en constante expansión, junto a la sede de las distintas Consejerías de la Junta de Extremadura.</p>

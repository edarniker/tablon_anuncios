                <p style="text-align: justify;">Nuestro Coro, <em><a href="https://www.youtube.com/user/IesAlbarregas">canal en Youtube</a></em> y nuestra implicación en la vida social, cultural y deportiva de Mérida, hacen que el nombre del Albarregas suene constantemente y sea motivo de orgullo para todos los alumn@s y sus familias.<br />
                    La educación hoy en día es cosa de tod@s y, en ese sentido, la participación  delas familias es fundamental. El Albarregas es lugar de encuentro para el alumnado, las familias, el profesorado y todo el personal de nuestro Centro, siendo el AMPA y el Consejo Escolar motor de nuestra Comunidad Educativa.</p>

